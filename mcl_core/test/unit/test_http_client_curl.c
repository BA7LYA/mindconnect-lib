{
    mcl_error_t code;
    mcl_http_client_t *http_client = MCL_NULL;

    mcl_http_client_configuration_t http_configuration;
    http_configuration.port = 443;
    http_configuration.certificate = mindsphere_certificate;
    http_configuration.proxy_hostname = "Test_proxy_hostname";
    http_configuration.proxy_username = "Test_proxy_username";
    http_configuration.proxy_domain = "Test_proxy_domain";
    http_configuration.proxy_password = "Test_proxy_password";
    http_configuration.user_agent = "Test";
    http_configuration.http_request_timeout = 60;

    // Initialize http client.
    code = mcl_http_client_initialize(&http_configuration, &http_client);
    TEST_ASSERT_MESSAGE(MCL_OK == code, "HTTP client could not be initialized.");

    // Test.
    code = mcl_http_client_add_certificate(http_client, another_certificate, MCL_FALSE);
    TEST_ASSERT_MESSAGE(MCL_OK == code, "Certificate could not be added.");

    // Clean up.
    mcl_http_client_destroy(&http_client);
    TEST_ASSERT_MESSAGE(MCL_NULL == http_client, "HTTP client is not NULL after destroying.");
}
